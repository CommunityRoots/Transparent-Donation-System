$("#showAddCharity").click(function() {
    $("#addCharity").toggle();
    $("#addLeader").hide();
});
$("#closeAddCharity").click(function() {
    $("#addCharity").hide();
});
$("#showAddLeader").click(function() {
    $("#addLeader").toggle();
    $("#addCharity").hide();
});
$("#closeAddLeader").click(function() {
    $("#addLeader").hide();
});



